#Break statement.
for i in range(12):
    if(i == 10):
        print("Loop Break")
        break
    print("5 X", i +1, "=", 5 * (i+1))


#Continue statement.
for i in range(12):
    if(i == 10):
         print("skip the itration")    
         continue  
    print("5 X", i +1, "=", 5 * (i+1))

    