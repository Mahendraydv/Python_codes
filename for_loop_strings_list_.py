#for loop with string. 
name = "mosam"
for i in name:
    print(i)

#for loop with list.
colors = ["Red" , "Green" , "Blue"]
for k in colors:
    print(k)    

#Range().
#range(stop)
for k in range(5):
    print(k)   

#range(start, stop)
for i in range(2, 15):
    print(i, end= (" "))

#range(start, stop, step)
for i in range(2, 20, 2):
    print(i, end= " ")  
      